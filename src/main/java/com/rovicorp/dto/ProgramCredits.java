package com.rovicorp.dto;

public class ProgramCredits {
	
	private String programID;
	private String creditID;
	private String creditType;
	private String firstName;
	private String lastName;
	private String fullCreditName;
	private String partName;
	private String sequenceNumber;
	
	public String getProgramID() {
		return programID;
	}
	public void setProgramID(String programID) {
		this.programID = programID;
	}
	public String getCreditID() {
		return creditID;
	}
	public void setCreditID(String creditID) {
		this.creditID = creditID;
	}
	public String getCreditType() {
		return creditType;
	}
	public void setCreditType(String creditType) {
		this.creditType = creditType;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getFullCreditName() {
		return fullCreditName;
	}
	public void setFullCreditName(String fullCreditName) {
		this.fullCreditName = fullCreditName;
	}
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public String getSequenceNumber() {
		return sequenceNumber;
	}
	public void setSequenceNumber(String sequenceNumber) {
		this.sequenceNumber = sequenceNumber;
	}
}
